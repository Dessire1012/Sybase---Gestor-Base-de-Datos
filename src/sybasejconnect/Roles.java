    private void AgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AgregarMouseClicked
        if(permiso){
            String item = jList1.getSelectedValue();
            int index = item.indexOf("-");
            id_rol = String.valueOf(item.substring(0, index));
            query = "Insert into Dessi.Usuario_Rol\n" +
                    "Values ("+id_usuario+","+id_rol+")";

            try {
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Rol agregado con exito");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "No se pudo agregar el rol\n");
            }

            try {
                LlenarLista("Rol_Usuario");
            } catch (SQLException ex) {
                Logger.getLogger(Roles.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "No tiene permiso para agregar roles");
        }
        
    }//GEN-LAST:event_AgregarMouseClicked

