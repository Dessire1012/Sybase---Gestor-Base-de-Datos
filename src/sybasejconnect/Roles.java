    void LlenarLista(String nombreLista) throws SQLException{
            query = "";
                      
            if(nombreLista.equalsIgnoreCase("Rol_Sistema")){
                modelo = (DefaultListModel)jList1.getModel();
                query = "select * from Rol";
            }else if(nombreLista.equalsIgnoreCase("Rol_Usuario")){
                modelo = (DefaultListModel)jList2.getModel();
                query = "Select r1.Id_Rol, r1.Nombre from Usuario_Rol u1\n" +
                        "inner join Rol r1 on u1.Id_Rol = r1.Id_Rol\n" +
                        "inner join Usuario u2 on u2.Id_Usuario = u1.Id_Usuario and u2.Id_Usuario =  "+id_usuario; 
            }
            
            modelo.removeAllElements();   
            rs = st.executeQuery(query);
            while (rs.next()){
                modelo.addElement(rs.getString(1) + "-"+ rs.getString(2));
            }
    }

